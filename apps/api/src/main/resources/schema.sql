  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_agent_log_uploads_user_id ON agent_log_uploads(user_id);
CREATE INDEX IF NOT EXISTS idx_agent_log_uploads_status ON agent_log_uploads(status);
CREATE INDEX IF NOT EXISTS idx_agent_log_uploads_delete_after ON agent_log_uploads(delete_after);
CREATE INDEX IF NOT EXISTS idx_agent_log_uploads_created_at ON agent_log_uploads(created_at);

CREATE TABLE IF NOT EXISTS as_tickets (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id UUID NOT NULL UNIQUE DEFAULT gen_random_uuid(),
  user_id BIGINT NOT NULL REFERENCES users(id),
  log_upload_id BIGINT REFERENCES agent_log_uploads(id),
  assigned_admin_id BIGINT REFERENCES users(id),
  symptom TEXT NOT NULL,
  status VARCHAR(30) NOT NULL,
  cause_candidates JSONB,
  upgrade_candidates JSONB,
  admin_note TEXT,
  resolved_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ,
  deleted_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_as_tickets_user_id ON as_tickets(user_id);
CREATE INDEX IF NOT EXISTS idx_as_tickets_log_upload_id ON as_tickets(log_upload_id);
CREATE INDEX IF NOT EXISTS idx_as_tickets_status ON as_tickets(status);
CREATE INDEX IF NOT EXISTS idx_as_tickets_assigned_admin_id ON as_tickets(assigned_admin_id);
CREATE INDEX IF NOT EXISTS idx_as_tickets_created_at ON as_tickets(created_at);
CREATE INDEX IF NOT EXISTS idx_as_tickets_deleted_at ON as_tickets(deleted_at);

CREATE TABLE IF NOT EXISTS agent_sessions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id UUID NOT NULL UNIQUE DEFAULT gen_random_uuid(),
  user_id BIGINT NOT NULL REFERENCES users(id),
  requirement_id BIGINT REFERENCES requirements(id),
  build_id BIGINT REFERENCES builds(id),
  as_ticket_id BIGINT REFERENCES as_tickets(id),
  status VARCHAR(30) NOT NULL,
  summary TEXT,
  state_timeline JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_agent_sessions_user_id ON agent_sessions(user_id);
CREATE INDEX IF NOT EXISTS idx_agent_sessions_status ON agent_sessions(status);
CREATE INDEX IF NOT EXISTS idx_agent_sessions_requirement_id ON agent_sessions(requirement_id);
CREATE INDEX IF NOT EXISTS idx_agent_sessions_build_id ON agent_sessions(build_id);
CREATE INDEX IF NOT EXISTS idx_agent_sessions_as_ticket_id ON agent_sessions(as_ticket_id);

CREATE TABLE IF NOT EXISTS tool_invocations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id UUID NOT NULL UNIQUE DEFAULT gen_random_uuid(),
  agent_session_id BIGINT NOT NULL REFERENCES agent_sessions(id),
  tool_name VARCHAR(50) NOT NULL,
  status VARCHAR(30) NOT NULL,
  confidence VARCHAR(30) NOT NULL,
  summary TEXT NOT NULL,
  request_payload JSONB,
  result_payload JSONB,
  latency_ms INTEGER,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_tool_invocations_agent_session_id ON tool_invocations(agent_session_id);
CREATE INDEX IF NOT EXISTS idx_tool_invocations_tool_status ON tool_invocations(tool_name, status);
CREATE INDEX IF NOT EXISTS idx_tool_invocations_created_at ON tool_invocations(created_at);

CREATE TABLE IF NOT EXISTS rag_evidence (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id UUID NOT NULL UNIQUE DEFAULT gen_random_uuid(),
  agent_session_id BIGINT REFERENCES agent_sessions(id),
  source_id VARCHAR(120) NOT NULL,
  chunk_text TEXT NOT NULL,
  summary TEXT NOT NULL,
  score NUMERIC(8, 5),
  embedding vector(1536),
  metadata JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_rag_evidence_agent_session_id ON rag_evidence(agent_session_id);
CREATE INDEX IF NOT EXISTS idx_rag_evidence_source_id ON rag_evidence(source_id);
CREATE INDEX IF NOT EXISTS idx_rag_evidence_score ON rag_evidence(score);

